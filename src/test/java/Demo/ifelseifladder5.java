package Demo;

public class ifelseifladder5 {

	public static void main(String[] args) {
	   
		int number = 12;
		
		if (number >0) {
			
			System.out.println("number is positive");
			
		}
		
		else if (number <0) {
			System.out.println("number is negative");
		}
		
		else {
			System.out.println("number is zero");
		}

	}

}
